# Define variables
a <- 10
b <- 3

# Perform basic arithmetic operations
addition <- a + b
subtraction <- a - b
multiplication <- a * b
division <- a / b
exponentiation <- a ^ b
modulus <- a %% b

# Print the results
print(paste("Addition:", addition))            # Output: Addition: 13
print(paste("Subtraction:", subtraction))      # Output: Subtraction: 7
print(paste("Multiplication:", multiplication)) # Output: Multiplication: 30
print(paste("Division:", division))            # Output: Division: 3.333333
print(paste("Exponentiation:", exponentiation)) # Output: Exponentiation: 1000
print(paste("Modulus:", modulus))              # Output: Modulus: 1
