# Using the <- operator
x <- 10                # Numeric variable
name <- "Alice"        # Character variable
is_valid <- TRUE       # Logical variable
values <- c(1, 2, 3, 4) # Vector of numeric values

# Using the = operator
age = 25                # Numeric variable
city = "New York"       # Character variable
has_pets = FALSE        # Logical variable
grades = c(90, 85, 88)  # Vector of numeric values

# Print the variables to check their values
print(x)
print(name)
print(is_valid)
print(values)
print(age)
print(city)
print(has_pets)
print(grades)
